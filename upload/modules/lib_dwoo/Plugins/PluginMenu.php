function PluginMenu(Dwoo\Core $core)
{
    $attr = func_get_args();
    // first attr is $Dwoo
    array_shift($attr);
    // second attr is menu (id or name)
    $menu = array_shift($attr);
    return \CAT\Helper\Menu::show($menu,$attr);
}